package practice;

public class Age {
    public static void main(String[] args) {

        int myAge = 30;
        int mySisterAge = 30;

        if (myAge == mySisterAge) {
            System.out.println("We are the same age");
        } else if (myAge > mySisterAge) {
            System.out.println("I am older than my sister");
        } else {
            System.out.println("Sister is older than me");
        }

    }
}
